from django.contrib.auth.models import User
from django.urls import reverse
from rest_framework import status
from rest_framework.test import APITestCase


class AccountsTest(APITestCase):
    def setUp(self):
        # Vamos definir o usuário que será criado
        self.test_user = User.objects.create_user('testuser', 'test@example.com', 'testpassword')

        # URL para criar uma conta
        self.create_url = reverse('create-user')

    def test_create_user(self):
        """
        Garante que é possível criar um novo usuário e que um token válido é gerado.
        """
        data = {
            'username': 'foobar',
            'email': 'foobar@example.com',
            'password': 'somepassword'
        }

        response = self.client.post(self.create_url, data, format='json')

        # Precisamos ter certeza que existem 2 usuários no banco
        self.assertEqual(User.objects.count(), 2)
        # Confirmamos o retorno 201
        self.assertEqual(response.status_code, status.HTTP_201_CREATED)
        # E para garantir, verificamos se o retorno da chamada contém os dados de username e email do usuário
        self.assertEqual(response.data['username'], data['username'])
        self.assertEqual(response.data['email'], data['email'])

    def test_create_user_with_short_password(self):
        """
        Gartante que a senha do usuário tenha mais de 8 caracteres
        """
        data = {
            'username': 'foobar',
            'email': 'foobarbaz@example.com',
            'password': 'foo'
        }

        response = self.client.post(self.create_url, data, format='json')
        self.assertEqual(response.status_code, status.HTTP_400_BAD_REQUEST)
        self.assertEqual(User.objects.count(), 1)
        self.assertEqual(len(response.data['password']), 1)

